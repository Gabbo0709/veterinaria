class Producto {
  constructor (id, tipo, nombre, precio, fechaIngreso, fechaCaducidad, proveedorId) {
    this.id = id
    this.tipo = tipo
    this.nombre = nombre
    this.precio = precio
    this.fechaIngreso = fechaIngreso
    this.fechaCaducidad = fechaCaducidad
    this.proveedorId = proveedorId
  }
}

export default Producto
